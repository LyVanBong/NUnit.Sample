namespace NUnit.Sample
{
    public class Calculator
    {
        /// <summary>
        /// Phép tính cộng
        /// </summary>
        /// <param name="x"></param>
        /// <param name="y"></param>
        /// <returns></returns>
        public int Add(int x, int y)
        {
            return x + y;
        }
        /// <summary>
        /// Phép tính trừ
        /// </summary>
        /// <param name="x"></param>
        /// <param name="y"></param>
        /// <returns></returns>
        public int Subtract(int x, int y)
        {
            return x - y;
        }
    }
}